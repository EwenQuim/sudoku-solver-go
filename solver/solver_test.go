func TestSolve(t *testing.T) {
	toSolve := [9][9]uint8{
		{0, 0, 0, 0, 0, 0, 1, 0, 0},
		{3, 0, 1, 7, 9, 0, 0, 0, 0},
		{0, 4, 0, 0, 0, 0, 0, 0, 7},
		{0, 0, 5, 0, 0, 7, 3, 0, 0},
		{7, 0, 0, 5, 0, 2, 0, 0, 0},
		{0, 0, 8, 0, 1, 0, 2, 0, 0},
		{6, 0, 7, 0, 0, 9, 0, 3, 0},
		{0, 1, 0, 2, 0, 0, 0, 5, 0},
		{0, 0, 9, 0, 0, 0, 0, 0, 8},
	}
	result := [9][9]uint8{
		{9, 7, 2, 6, 5, 8, 1, 4, 3},
		{3, 8, 1, 7, 9, 4, 5, 2, 6},
		{5, 4, 6, 3, 2, 1, 9, 8, 7},
		{1, 2, 5, 8, 6, 7, 3, 9, 4},
		{7, 9, 3, 5, 4, 2, 8, 6, 1},
		{4, 6, 8, 9, 1, 3, 2, 7, 5},
		{6, 5, 7, 1, 8, 9, 4, 3, 2},
		{8, 1, 4, 2, 3, 6, 7, 5, 9},
		{2, 3, 9, 4, 7, 5, 6, 1, 8},
	}

	solved := Solve(toSolve)

	if solved != result {
		t.Errorf("Not solved")
	}
}

